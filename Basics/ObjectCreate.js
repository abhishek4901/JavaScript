// Creating an object using object literal
let person = {
    name: "raj",
    age: 25,
    city: "Delhi"
};

// Accessing object  
console.log(person);//{ name: 'raj', age: 25, city: 'Delhi' }
// Accessing object  keys
console.log("Name:", person.name);//raj
console.log("Age:", person.age);//25
console.log(person["name"]);//raj
console.log(person["age"]);//25